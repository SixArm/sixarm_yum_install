## Favorite Fonts
sudo yum install -y ttf-aenigma `# 465 free TrueType fonts by Brian Kent`
sudo yum install -y ttf-georgewilliams `# Free unicode TrueType fonts by George Williams`
sudo yum install -y ttf-liberation `# Fonts with the same metrics as Times, Arial and Courier`
sudo yum install -y ttf-mscorefonts-installer `# Installer for Microsoft TrueType core fonts`
sudo yum install -y ttf-sjfonts `# Some Juicy Fonts handwriting fonts`

